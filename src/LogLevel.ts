/**
 * Defines all loglevels.
 * @json
 */
export enum LogLevel {
    /**
     * No logging
     */
    None,
    /**
     * Debug level (internal details are displayed).
     */
    Debug,
    /**
     * Info level (only important details are shown)
     */
    Info,
    /**
     * Warning level
     */
    Warning,
    /**
     * Error level.
     */
    Error
}