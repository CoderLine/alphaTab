/**
 * This public enumeration lists all feels of triplets.
 */
export enum TripletFeel {
    /**
     * No triplet feel
     */
    NoTripletFeel,
    /**
     * Triplet 16th
     */
    Triplet16th,
    /**
     * Triplet 8th
     */
    Triplet8th,
    /**
     * Dotted 16th
     */
    Dotted16th,
    /**
     * Dotted 8th
     */
    Dotted8th,
    /**
     * Scottish 16th
     */
    Scottish16th,
    /**
     * Scottish 8th
     */
    Scottish8th
}
