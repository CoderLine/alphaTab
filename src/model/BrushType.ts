/**
 * Lists all types of how to brush multiple notes on a beat.
 */
export enum BrushType {
    /**
     * No brush.
     */
    None,
    /**
     * Normal brush up.
     */
    BrushUp,
    /**
     * Normal brush down.
     */
    BrushDown,
    /**
     * Arpeggio up.
     */
    ArpeggioUp,
    /**
     * Arpeggio down.
     */
    ArpeggioDown
}
