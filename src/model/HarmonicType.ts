/**
 * Lists all harmonic types.
 */
export enum HarmonicType {
    /**
     * No harmonics.
     */
    None,
    /**
     * Natural harmonic
     */
    Natural,
    /**
     * Artificial harmonic
     */
    Artificial,
    /**
     * Pinch harmonics
     */
    Pinch,
    /**
     * Tap harmonics
     */
    Tap,
    /**
     * Semi harmonics
     */
    Semi,
    /**
     * Feedback harmonics
     */
    Feedback
}
