/**
 * Lists all layout modes that are supported.
 */
 export enum LayoutMode {
    /**
     * Bars are aligned in rows using a fixed width. [Demo](https://alphatab.net/docs/showcase/layouts#page-layout)
     */
    Page,
    /**
     * Bars are aligned horizontally in one row. [Demo](https://alphatab.net/docs/showcase/layouts#horizontal-layout)
     */
    Horizontal
}